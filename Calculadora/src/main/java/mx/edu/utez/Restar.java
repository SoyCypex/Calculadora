package mx.edu.utez;

public class Restar {
    private double num;
    private double num2;
    public double calculaResta(double num, double num2) {
        return num - num2;
    }
    public double getNum() {
        return num;
    }
    public void setNum(double num) {
        this.num = num;
    }
    public double getNum2() {
        return num2;
    }
    public void setNum2(double num2) {
        this.num2 = num2;
    }
}

